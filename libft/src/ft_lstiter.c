#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*next;
	t_list	*tmp;

	tmp = lst;
	while (tmp)
	{
		next = tmp->next;
		f(tmp);
		tmp = next;
	}
}
