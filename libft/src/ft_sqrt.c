#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 0)
		return (0);
	if (nb > 46340 * 46340)
		return (0);
	while (i <= 46340 && i <= (nb / 2))
	{
		if (i * i == nb)
			return (i);
		else
			i++;
	}
	return (0);
}
