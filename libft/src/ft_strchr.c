#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	c = (char)c;
	while (s[i] != c)
	{
		if (s[i] == '\0' && c != 0)
			return (0);
		i++;
	}
	return ((char*)(s + i));
}
